import fs from "node:fs/promises";
import path from "node:path";

import gplay from "google-play-scraper";

const rootDir = process.cwd();
const dataFilePath = path.join(rootDir, "data", "games.generated.ts");
const publicGamesDir = path.join(rootDir, "public", "images", "games");

const gameConfigs = [
  {
    slug: "skincare-asmr-spa",
    appId: "com.relaxing.skincare.asmr.spa.makeover.salon"
  },
  {
    slug: "shuriken-slice",
    appId: "com.thegamebeast.shuriken.slice.memerot.games"
  },
  {
    slug: "scary-survival-house",
    appId: "com.scarysurvival.house"
  },
  {
    slug: "fire-shooting-battle-royale",
    appId: "com.fireshooting.battleroyale"
  },
  {
    slug: "harvest-crop-farm-town",
    appId: "com.harvestcrop.farmtown"
  }
];

const EXTENSION_BY_CONTENT_TYPE = {
  "image/jpeg": ".jpg",
  "image/jpg": ".jpg",
  "image/png": ".png",
  "image/webp": ".webp"
};

function toPosixPath(filePath) {
  return filePath.split(path.sep).join("/");
}

async function ensureDir(dirPath) {
  await fs.mkdir(dirPath, { recursive: true });
}

async function clearGeneratedFiles(dirPath) {
  try {
    const entries = await fs.readdir(dirPath);
    await Promise.all(
      entries
        .filter((entry) => entry !== ".gitkeep")
        .map((entry) => fs.rm(path.join(dirPath, entry), { recursive: true, force: true }))
    );
  } catch {
    await ensureDir(dirPath);
  }
}

async function downloadImage(url, outputPathWithoutExtension) {
  const response = await fetch(url, {
    headers: {
      "user-agent": "Mozilla/5.0 (compatible; PortfolioAssetSync/1.0)"
    }
  });

  if (!response.ok) {
    throw new Error(`Failed to download asset: ${url} (${response.status})`);
  }

  const contentType = response.headers.get("content-type")?.split(";")[0]?.trim() ?? "";
  const extension =
    EXTENSION_BY_CONTENT_TYPE[contentType] ?? path.extname(new URL(url).pathname) ?? ".jpg";
  const safeExtension = extension || ".jpg";
  const outputPath = `${outputPathWithoutExtension}${safeExtension}`;
  const arrayBuffer = await response.arrayBuffer();

  await fs.writeFile(outputPath, Buffer.from(arrayBuffer));

  return outputPath;
}

async function syncGameAssets({ slug, appId }) {
  const app = await gplay.app({
    appId,
    lang: "en",
    country: "us"
  });

  const gameDir = path.join(publicGamesDir, slug, "store");
  await ensureDir(gameDir);
  await clearGeneratedFiles(gameDir);

  const screenshots = Array.isArray(app.screenshots) ? app.screenshots.filter(Boolean).slice(0, 4) : [];

  if (!app.icon || screenshots.length === 0) {
    throw new Error(`Missing icon or screenshots for ${appId}`);
  }

  const iconPath = await downloadImage(app.icon, path.join(gameDir, "icon"));
  const screenshotPaths = [];

  for (const [index, screenshotUrl] of screenshots.entries()) {
    const screenshotPath = await downloadImage(
      screenshotUrl,
      path.join(gameDir, `screenshot-${index + 1}`)
    );
    screenshotPaths.push(screenshotPath);
  }

  return {
    slug,
    icon: `/${toPosixPath(path.relative(path.join(rootDir, "public"), iconPath))}`,
    headerImage: `/${toPosixPath(path.relative(path.join(rootDir, "public"), screenshotPaths[0]))}`,
    screenshots: screenshotPaths.map(
      (screenshotPath) => `/${toPosixPath(path.relative(path.join(rootDir, "public"), screenshotPath))}`
    ),
    syncedAt: new Date().toISOString()
  };
}

function buildGeneratedFile(assetEntries) {
  return `export type GeneratedGameAssets = {
  slug: string;
  icon: string;
  headerImage: string;
  screenshots: string[];
  syncedAt: string;
};

// This file is generated by scripts/sync-playstore-assets.mjs.
export const generatedGameAssets: GeneratedGameAssets[] = ${JSON.stringify(assetEntries, null, 2)};\n`;
}

async function main() {
  const assetEntries = [];

  for (const config of gameConfigs) {
    const syncedAssets = await syncGameAssets(config);
    assetEntries.push(syncedAssets);
    console.log(`Synced assets for ${config.slug}`);
  }

  await fs.writeFile(dataFilePath, buildGeneratedFile(assetEntries), "utf8");
  console.log(`Updated ${path.relative(rootDir, dataFilePath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
